/**
 * @brief ボタン初期化
 * @param config 初期化設定
 * @return 生成したボタンのインスタンス
 */
BUTTON_class_t BUTTON_initialize_instance(BUTTON_config_t config)
{
    BUTTON_class_t button_instance;

    button_instance.config = config;
    button_instance.state_push = false;

    return button_instance;
}

